# Instructor Class

class Instructor:

    # Constructor method to initialize attributes
    def __init__(self, name, surname, birth_day, address, phone, img, ident=None):
        self.name = name
        self.surname = surname
        self.birth_day = birth_day
        self.address = address
        self.phone = phone
        self.img = img
        self.ident = ident
